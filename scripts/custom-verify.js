function getCustomVerifyObj() {
	// 这里既可以是函数，也可以是数组
	// 数组的两个值分别代表：[正则匹配、匹配不符时的提示文字]
	var obj = {
		space: [/^\S/, '不能包含空格！'],
		zh: [/[^\u4e00-\u9fa5]/, '不能包含中文！'],
		symbol: [/^\w+$/, '不能包含特殊字符、标点符号！'],
		length_4_10: [/.{5,11}/, '请输入 4 -10 个字符'],
		length_6_30: [/.{7,31}/, '请输入 6 -30 个字符'],
	}
	return obj;
}